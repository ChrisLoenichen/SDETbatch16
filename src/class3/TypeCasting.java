package class3;

public class TypeCasting {//casting is converting one data type to another
    public static void main(String[] args) {

        byte num=127;
        short num4=num;
        System.out.println(num4);

        int num5=11111;
        double num6=num5;
        System.out.println(num6);

        double numb=100.5;
        int numb2=(int)numb;
        System.out.println(numb2);
    }
}
