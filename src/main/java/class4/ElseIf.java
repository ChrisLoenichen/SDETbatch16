package class4;

public class ElseIf {
    public static void main(String[] args) {
        int day = 1;
        if (day == 1) {
            System.out.println("monday");
        } else if (day == 2) {
            System.out.println("tues");
        } else if (day == 3) {
            System.out.println("wed");
        } else if (day == 4) {
            System.out.println("thurs");

        }
    }
}
