package class4;

public class E4ElseIf {
    public static void main(String[] args) {
        String fruit = "Tomato";

        if (fruit.equals("Orange")) {
            System.out.println("its 8 dollars");
        } else if (fruit.equals("Banana")) {
            System.out.println("Its 2 dollars");
        } else if (fruit.equals("Tomato")) {
            System.out.println("pay 3");
        } else {
            System.out.println("got no fruit");
        }
    }
}
